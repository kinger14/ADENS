def calcular_nivel():
    nuevo_nivel = (usuario["xp"] // 50) + 1
    if nuevo_nivel > usuario["nivel"]:
        usuario["nivel"] = nuevo_nivel
        return True
    return False

def actualizar_tarjeta_perfil(notificacion_nivel=""):
    with pantalla_perfil:
        clear_output(wait=True)
        juegos_ganados = len(usuario["niveles_aprobados"])
        barra_progreso_total.value = juegos_ganados
        porcentaje_total = int((juegos_ganados / 200) * 100)

        tarjeta_html = widgets.HTML(value=f"""
        <div style="background-color: #2c3e50; color: white; padding: 15px; border-radius: 10px; margin-bottom: 10px;">
            <div style="{usuario['avatar_estilo']} width: 60px; height: 60px; border-radius: 50%; float: left; margin-right: 15px; border: 3px solid white;"></div>
            <h2 style="margin: 0; padding-top: 5px;">{usuario['nombre']}</h2>
            <p style="margin: 3px 0 0 0; font-weight: bold; color: #f1c40f;">🌟 Nivel {usuario['nivel']} | ✨ {usuario['xp']} XP</p>
            <p style="margin: 5px 0 0 0; font-size: 12px; color: #bdc3c7;">Completado: {juegos_ganados}/200 minijuegos ({porcentaje_total}%)</p>
            <div style="clear: both;"></div>
        </div>
        {notificacion_nivel}
        """)
        display(tarjeta_html, barra_progreso_total, btn_cerrar_sesion)

def inicializar_menus():
    global bloqueo_eventos
    bloqueo_eventos = True

    opciones_materia = {}
    for clave, datos in banco_materias.items():
        resueltos_esta_materia = sum(1 for x in usuario["niveles_aprobados"] if x.startswith(clave))
        opciones_materia[f"{datos['menu_texto']} ({resueltos_esta_materia}/50)"] = clave

    seleccion_actual = menu_desplegable.value
    menu_desplegable.options = opciones_materia
    if seleccion_actual in menu_desplegable.options.values():
        menu_desplegable.value = seleccion_actual

    avatares_disponibles = {}
    for nombre, datos in lista_avatares.items():
        if usuario["nivel"] >= datos["nivel_req"]:
            avatares_disponibles[nombre] = datos["estilo"]
        else:
            avatares_disponibles[f"🔒 {nombre} (Nivel {datos['nivel_req']})"] = "BLOQUEADO"

    selector_avatar.options = avatares_disponibles
    selector_avatar.value = usuario["avatar_estilo"]

    with pantalla_controles:
        clear_output(wait=True)
        display(menu_desplegable)

    with pantalla_ajustes:
        clear_output(wait=True)
        display(panel_ajustes)

    bloqueo_eventos = False

def al_cambiar_materia(change):
    if bloqueo_eventos: return
    if change['type'] == 'change' and change['name'] == 'value' and change['new']:
        cargar_leccion(change['new'])

def al_cambiar_avatar(change):
    if bloqueo_eventos: return
    if change['type'] == 'change' and change['name'] == 'value':
        if change['new'] and change['new'] != "BLOQUEADO":
            usuario["avatar_estilo"] = change['new']
            actualizar_tarjeta_perfil()
            guardar_progreso_usuario() # Guarda el cambio estético de avatar

def al_cerrar_sesion(b):
    # Limpia la pantalla completa y nos regresa al formulario de registro de la Celda 4
    clear_output()
    usuario["nombre"] = ""
    print("🔄 Sesión cerrada. Por favor, ejecuta la Celda 4 para iniciar sesión con otro nombre.")

btn_cerrar_sesion.on_click(al_cerrar_sesion)

def cargar_leccion(id_materia):
    nivel_actual = 1
    for i in range(1, 51):
        if f"{id_materia}_{i}" not in usuario["niveles_aprobados"]:
            nivel_actual = i
            break

    if f"{id_materia}_50" in usuario["niveles_aprobados"]:
        with pantalla_leccion:
            clear_output(wait=True)
            display(widgets.HTML(f"<h2>🏆 ¡Felicidades!</h2><p>Completaste los 50 minijuegos de esta materia.</p>"))
        return

    datos = banco_materias[id_materia]["juegos"][nivel_actual - 1]

    texto_teoria = widgets.HTML(value=f"<h2>📚 {datos['titulo']}</h2><p style='font-size:14px; white-space: pre-wrap;'>{datos['explicacion']}</p>")
    btn_ir_a_jugar = widgets.Button(description="¡Entendido! Jugar 🎮", button_style='success')
    pestana_aprender = widgets.VBox([texto_teoria, btn_ir_a_jugar])

    texto_juego = widgets.HTML(value=f"<h3>🧩 Minijuego ({nivel_actual}/50)</h3><p>{datos['pregunta']}</p>")
    selector = widgets.RadioButtons(options=datos['opciones'], value=None, description='Respuesta:')
    btn_comprobar = widgets.Button(description="Comprobar Respuesta", button_style='info')
    texto_feedback = widgets.HTML(value="")
    contenedor_audio_local = widgets.Output()

    pestana_juego = widgets.VBox([texto_juego, selector, btn_comprobar, texto_feedback, contenedor_audio_local])
    tabs_leccion = widgets.Tab(children=[pestana_aprender, pestana_juego])
    tabs_leccion.set_title(0, '1. Teoría')
    tabs_leccion.set_title(1, '2. Jugar')

    btn_ir_a_jugar.on_click(lambda b: setattr(tabs_leccion, 'selected_index', 1))

    def verificar(b):
        if selector.value == datos['correcta']:
            with contenedor_audio_local:
                clear_output(wait=True)
                display(generar_sonido_acierto())

            clave_nivel = f"{id_materia}_{nivel_actual}"
            aviso_nivel = ""

            if clave_nivel not in usuario["niveles_aprobados"]:
                usuario["niveles_aprobados"].add(clave_nivel)
                usuario["xp"] += 1

                subio = calcular_nivel()
                if subio:
                    aviso_nivel = f"<div style='background-color:#2ecc71; color:white; padding:10px; border-radius:5px; margin-top:5px; font-weight:bold;'>🎉 ¡Subiste de nivel! Nuevo escudo disponible.</div>"

                guardar_progreso_usuario() # 💾 GUARDA PROGRESO TRAS CADA ACIERTO
                actualizar_tarjeta_perfil(aviso_nivel)
                inicializar_menus()

            texto_feedback.value = f"<span style='color:green; font-weight:bold;'>¡Correcto! (+1 XP). Guardando progreso y cargando siguiente nivel...</span>"
            b.disabled = True
            time.sleep(1)
            cargar_leccion(id_materia)
        else:
            with contenedor_audio_local:
                clear_output(wait=True)
                display(generar_sonido_error())
            texto_feedback.value = "<span style='color:red; font-weight:bold;'>Respuesta incorrecta. Revisa la teoría e intenta de nuevo. 🔄</span>"

    btn_comprobar.on_click(verificar)

    with pantalla_leccion:
        clear_output(wait=True)
        display(tabs_leccion)



