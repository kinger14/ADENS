def generar_sonido_acierto():
    sr = 12000
    t = np.linspace(0, 0.3, int(sr * 0.3))
    frecuencia = 880
    onda = np.sin(2 * np.pi * frecuencia * t) * np.exp(-15 * t)
    return Audio(onda, rate=sr, autoplay=True)

def generar_sonido_error():
    sr = 12000
    t = np.linspace(0, 0.4, int(sr * 0.4))
    frecuencia = 120
    onda = np.sign(np.sin(2 * np.pi * frecuencia * t)) * np.exp(-4 * t)
    return Audio(onda, rate=sr, autoplay=True)

