import json
import os

# --- 💾 SISTEMA DE PERSISTENCIA (BASE DE DATOS EN ARCHIVO) ---
ARCHIVO_DATOS = "progreso_estudiantes.json"

def cargar_progreso_global():
    if os.path.exists(ARCHIVO_DATOS):
        with open(ARCHIVO_DATOS, "r", encoding="utf-8") as f:
            return json.load(f)
    return {}

def guardar_progreso_usuario():
    progreso_total = cargar_progreso_global()
    # Convertimos el conjunto (set) a una lista para que sea compatible con JSON
    datos_guardar = {
        "nombre": usuario["nombre"],
        "avatar_estilo": usuario["avatar_estilo"],
        "xp": usuario["xp"],
        "nivel": usuario["nivel"],
        "niveles_aprobados": list(usuario["niveles_aprobados"])
    }
    progreso_total[usuario["nombre"].lower().strip()] = datos_guardar
    with open(ARCHIVO_DATOS, "w", encoding="utf-8") as f:
        json.dump(progreso_total, f, ensure_ascii=False, indent=4)

